# Data types in python
# Integer (int): whole numbers 
age = 34 
# Float (float): decimal numbers
height = 5.11 
# String (str): sequence of characters
name = "Kartik" 
# Boolean (bool): True or False
is_student = True
# Complex (complex): complex numbers
complex_number = 3 + 4j
# List (list): ordered collection of items, mutable
friends = ["Alice", "Bob", "Charlie"]
# Tuple (tuple): ordered collection of items, immutable
coordinates = (10.0, 20.0)
# Dictionary (dict): unordered collection of key-value pairs
person = {"name": "Kartik", "age": 34, "is_student": True}
# Set (set): unordered collection of unique items
unique_numbers = {1, 2, 3, 4, 5}
